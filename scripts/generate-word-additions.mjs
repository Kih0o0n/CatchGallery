import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = path.join(root, "data", "word-additions-v130.json");
const outputPath = path.join(root, "word-additions-v130.js");
const expectedCounts = {
  "유명인": 86,
  "게임": 101,
  "게임 캐릭터": 127,
  "만화 / 애니 캐릭터": 143,
  "유명 캐릭터 / 마스코트": 97,
  "스타크래프트 1 유닛": 42
};

export function normalizeAnswer(value) {
  return String(value || "").trim().normalize("NFC").replace(/\s+/g, "").toLowerCase();
}

export function catalogKey(entry) {
  return `${entry.category}\u0000${entry.word}`;
}

export function validateDocument(document) {
  if (!document || document.schemaVersion !== 1 || document.releaseTarget !== "v1.3.0" || document.baseMainCommit !== "56024f729fc3b05b1bf151e55cfb03fffc26a6ab" || document.approved !== true) throw new Error("승인된 v1.3.0 제시어 문서가 아닙니다.");
  if (document.entryCount !== 596 || !Array.isArray(document.entries) || document.entries.length !== 596) throw new Error("신규 제시어는 정확히 596개여야 합니다.");
  if (JSON.stringify(document.categoryCounts) !== JSON.stringify(expectedCounts)) throw new Error("승인된 카테고리별 개수와 일치하지 않습니다.");
  const counts = Object.fromEntries(Object.keys(expectedCounts).map(category => [category, 0]));
  const keys = new Set();
  document.entries.forEach((entry, index) => {
    if (!entry || typeof entry.category !== "string" || !entry.category.trim()) throw new Error(`${index}: category가 올바르지 않습니다.`);
    if (typeof entry.word !== "string" || !entry.word.trim()) throw new Error(`${index}: word가 올바르지 않습니다.`);
    if (!Array.isArray(entry.answers) || entry.answers.length < 1 || entry.answers.length > 5 || entry.answers.some(answer => typeof answer !== "string" || !answer.trim())) throw new Error(`${index}: answers가 올바르지 않습니다.`);
    if (!Object.hasOwn(counts, entry.category)) throw new Error(`${index}: 승인되지 않은 category입니다.`);
    counts[entry.category] += 1;
    const normalizedAnswers = entry.answers.map(normalizeAnswer);
    if (!normalizedAnswers.includes(normalizeAnswer(entry.word))) throw new Error(`${index}: 대표 word가 answers에 없습니다.`);
    if (new Set(normalizedAnswers).size !== normalizedAnswers.length) throw new Error(`${index}: answers에 정규화 중복이 있습니다.`);
    const key = catalogKey(entry);
    if (keys.has(key)) throw new Error(`${index}: 동일 category 안에 중복 word가 있습니다.`);
    keys.add(key);
  });
  if (JSON.stringify(counts) !== JSON.stringify(expectedCounts)) throw new Error("실제 카테고리별 개수와 승인 개수가 다릅니다.");
  return document.entries;
}

export function generatedSource(entries) {
  return `// Generated by scripts/generate-word-additions.mjs. Do not edit directly.\n(function () {\n  "use strict";\n  const additions = ${JSON.stringify(entries, null, 2)};\n  window.CATCH_GALLERY_V130_WORD_ADDITIONS = additions;\n})();\n`;
}

if (process.argv[1] && import.meta.url === pathToFileURL(path.resolve(process.argv[1])).href) {
  const document = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
  const expected = generatedSource(validateDocument(document));
  if (process.argv.includes("--check")) {
    const actual = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
    if (actual !== expected) {
      console.error("word-additions-v130.js가 승인 JSON과 일치하지 않습니다. 생성기를 다시 실행하세요.");
      process.exitCode = 1;
    } else console.log("Generated word additions are up to date (596 entries).");
  } else {
    fs.writeFileSync(outputPath, expected, "utf8");
    console.log("Generated word-additions-v130.js (596 entries).");
  }
}
